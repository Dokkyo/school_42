int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	if (nb % 2 == 1)
	{
		while (i <= nb / i)
		{
			if (i * (nb / i) == nb)
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}
