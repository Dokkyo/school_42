int	ft_sqrt(int nb)
{
	int	a;
	int	res;

	a = 0;
	res = 0;
	if (nb <= 2147395600)
	{
		while (res < nb)
		{
			res = a * a;
			if (res == nb)
				return (a);
			a++;
		}
	}
	return (0);
}
